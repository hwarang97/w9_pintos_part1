#include "threads/loader.h"

OUTPUT_FORMAT("elf64-x86-64")
OUTPUT_ARCH(i386:x86-64)
ENTRY(_start)			/* kernelмқҖ "start" symbolм—җм„ң мӢңмһ‘н•ңлӢӨ. */

SECTIONS
{
  /* kernel baseмҡ© virtual addressлҘј м§Җм •н•ңлӢӨ. */
	. = LOADER_KERN_BASE + LOADER_PHYS_BASE;

	PROVIDE(start = .);
  /* kernelмқҖ codeлЎң мӢңмһ‘н•ҳкі  read-only dataмҷҖ writable dataк°Җ л’Өл”°лҘёлӢӨ. */
	.text : AT(LOADER_PHYS_BASE) {
		*(.entry)
		*(.text .text.* .stub .gnu.linkonce.t.*)
	} = 0x90
	.rodata         : { *(.rodata .rodata.* .gnu.linkonce.r.*) }

	. = ALIGN(0x1000);
	PROVIDE(_end_kernel_text = .);

  .data : { *(.data) *(.data.*)}

  /* BSS(0мңјлЎң мҙҲкё°нҷ”лҗң data)лҠ” лӢӨлҘё лӘЁл“  кІғ л’Өм—җ мһҲлӢӨ. */
  PROVIDE(_start_bss = .);
  .bss : { *(.bss) }
  PROVIDE(_end_bss = .);

  PROVIDE(_end = .);

	/DISCARD/ : {
		*(.eh_frame .note.GNU-stack .stab)
	}
}
